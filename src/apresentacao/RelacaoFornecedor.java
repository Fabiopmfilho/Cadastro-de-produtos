    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        DefaultTableModel dtm = (DefaultTableModel) jtbFornecedores.getModel();
        // Limpar a jTable
        for (int i = dtm.getRowCount()-1; i >= 0; i--) {
            dtm.removeRow(i);
        }
        DadosFornecedor dados = new DadosFornecedor();
        List fornecedores = dados.consultar();
        Iterator item = fornecedores.iterator();
        while (item.hasNext()) {
            Fornecedor f = (Fornecedor) item.next();
            dtm.addRow(new Object[] { f.getCnpj(), f.getRazaosocial(),
            f.getNomefantasia(), f.getDataabertura(), f.getCapital()
        });
      }
      jtbFornecedores.setModel(dtm);
    }//GEN-LAST:event_formWindowActivated

